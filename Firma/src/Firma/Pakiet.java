package Firma;

public class Pakiet {
    private Integer iloscPaczek;

    public Pakiet() {
        this.iloscPaczek = 0;
    }

    public Pakiet(Integer iloscPaczek) {
        this.iloscPaczek = iloscPaczek;
    }

    public Integer getIloscPaczek() {
        return iloscPaczek;
    }

    public void setIloscPaczek(Integer iloscPaczek) {
        this.iloscPaczek = iloscPaczek;
    }
}
